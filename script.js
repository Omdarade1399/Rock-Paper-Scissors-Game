//While-Loop to count from 1 to 10//

var num= 1;
while(num <= 10) {
  console.log(num);
  num++;
}

//Do-While-Loop to count from 1 to 10//

var num= 1;
do {
  console.log(num);
  num++;
}while (num <= 10)

//For-Loop to count from 1 to 10//

for(var num= 1; num <= 10; num++){
  console.log(num);
}

